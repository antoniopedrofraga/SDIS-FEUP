package db;

public class Owner {
	
	private int plateNumber;
	private String name;
	
	
	public int getPlateNumber() {
		return plateNumber;
	}
	
	public void setPlateNumber(int plateNumber) {
		this.plateNumber = plateNumber;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
}
